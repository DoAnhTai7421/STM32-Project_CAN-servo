/* Function to convert ADC value (0-4095) to angle (0-180 degrees) */
float ADC_To_Angle(uint16_t adc_value)
{
	return (adc_value / 4095.0f) * 180.0f;
}

/* Function to transmit sensor data via CAN */
void TransmitSensorData(void)
{
	uint16_t adc_val = HAL_ADC_GetValue(&hadc1);
	
	// Convert ADC value to angle (0-180)
	float angle = ADC_To_Angle(adc_val);
	uint16_t angle_int = (uint16_t)angle;  // Get integer value (degrees)

	// Pack angle value into 2 bytes
	TxData[0] = (angle_int >> 8) & 0xFF;
	TxData[1] = angle_int & 0xFF;
	// The remaining bytes can hold the value 0 or additional information
	for (int i = 2; i < 8; i++) {
			TxData[i] = 0;
	}

	// Configure CAN header with ID 0x002
	TxHeader.StdId = 0x002;
	TxHeader.IDE   = CAN_ID_STD;
	TxHeader.RTR   = CAN_RTR_DATA;
	TxHeader.DLC   = 8;  // Data length 8 bytes

	// Send CAN message
	if (HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox) != HAL_OK)
	{
			 char errorMsg[] = "CAN Transmit Error\r\n";
			 HAL_UART_Transmit(&huart1, (uint8_t*)errorMsg, strlen(errorMsg), HAL_MAX_DELAY);
	}
	else
	{
			 char msg[64];
			 sprintf(msg, "Sensor Data: ADC=%d, Angle=%d\r\n", adc_val, angle_int);
			 HAL_UART_Transmit(&huart1, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
	}
}

// CAN filter configuration
static void CAN_FilterConfig_Sensor(void)
{
    CAN_FilterTypeDef sFilterConfig;
    
    sFilterConfig.FilterBank = 0;  // Select filter bank 0
    sFilterConfig.FilterMode = CAN_FILTERMODE_IDMASK;
    sFilterConfig.FilterScale = CAN_FILTERSCALE_32BIT;
    
    /* Mask-style configuration: when mask = 0 then all bits are 'don't care'
		=> accept all messages */
    sFilterConfig.FilterIdHigh = 0x0000;
    sFilterConfig.FilterIdLow = 0x0000;
    sFilterConfig.FilterMaskIdHigh = 0x0000;
    sFilterConfig.FilterMaskIdLow = 0x0000;
    
    sFilterConfig.FilterFIFOAssignment = CAN_RX_FIFO0;
    sFilterConfig.FilterActivation = ENABLE;
    sFilterConfig.SlaveStartFilterBank = 14;  

    if (HAL_CAN_ConfigFilter(&hcan, &sFilterConfig) != HAL_OK)
    {
         /* Error handling if filter configuration fails */
         Error_Handler();
    }
}

